package MidTerm2011_12;
/**
 * Class containing constructor of position object with details of particle
 * @author Steven Vuong
 * @version 01/01/2018
 */
public class Position {

	//Initialise variables
	double x; double y; double z;
	
	/**
	 * Constructor which takes details and puts into one position object
	 * @param double x
	 * @param double y
	 * @param double z
	 */
	public Position(double x, double y, double z) {
		this.x = x;
		this.y = y;
		this.z = z;
	}

	
	
}
